/**
 * Decompile view component.
 *
 * Shows Ghidra-decompiler output for the function containing (or starting
 * at) the current seek address. The heavy lifting runs in a Web Worker
 * that owns the Ghidra wasm module — see web/decompiler/worker.js.
 *
 * The client is created lazily on first use and kept alive for the
 * session. Spec files stream in via Emscripten's lazy FS, so the first
 * decompile on a new arch pays the fetch cost and subsequent ones are
 * fast.
 */

import { events, Events } from '../core/events.js';
import { getWasmAPI } from '../core/wasm-api.js';
import { escapeHtml, parseAddress } from '../utils/format.js';
import { DecompilerClient } from '/decompiler/client.js';

// SLEIGH base ID comes from the wasm session
// (`api.session.sleigh_language_id()`) so the resolution rules live in
// one place — see Session::sleigh_language_id_for in src/session/mod.rs.

// Map file format to Ghidra compiler-spec id. Mirrors the same mapping
// in src/session/mod.rs — ELF→gcc, PE→windows, Mach-O→gcc.
function compilerFor(format) {
    switch (format) {
        case 'PE': return 'windows';
        case 'Mach-O': return 'gcc';
        case 'ELF':
        default: return 'gcc';
    }
}

// Debounce window for re-decompiling after a seek. Fast enough to feel
// live; slow enough that rapid-fire arrow-key navigation doesn't queue
// up a pile of already-stale worker requests.
const SEEK_DEBOUNCE_MS = 150;

// Extend Prism's C grammar with the token shapes Ghidra's decompiler
// emits. Idempotent — safe to call repeatedly; if Prism isn't loaded
// yet we skip and fall back to plain text. We gate with a module-level
// flag rather than a property on the grammar object because Prism
// iterates ALL enumerable properties of the grammar as token rules and
// would crash calling `.exec()` on our sentinel.
let ghidraGrammarPatched = false;
function ensureGhidraGrammar() {
    if (typeof window === 'undefined' || !window.Prism?.languages?.c) return null;
    if (ghidraGrammarPatched) return window.Prism.languages.c;

    // Ghidra's sized-int types + common synthetic identifiers. Inserted
    // *before* C's built-in `keyword` rule so our patterns win priority
    // over `int` / `char` / etc. without masking them.
    window.Prism.languages.insertBefore('c', 'keyword', {
        'ghidra-type': /\b(?:(?:u?int|float|undefined|xunknown)[1-9]\d?|[su]?(?:byte|word|dword|qword))\b/,
        // Autogenerated symbols: FUN_4005c5, SUB_…, DAT_…, LAB_…, PTR_…,
        // UNK_…, OFF_…
        'ghidra-synth': /\b(?:FUN|SUB|DAT|LAB|PTR|UNK|OFF)_[0-9a-fA-F]+\b/,
        // Decompiler-inferred locals / params: iVar1, pcVar3, uVar2,
        // param_1, iStack_c, unaff_EDI, in_FS_OFFSET, out_EAX.
        'ghidra-var': /\b(?:[a-z]{1,3}Var\d+|param_\d+|[a-z]Stack_[0-9a-fA-F]+|(?:unaff|in|out)_[A-Z][A-Z0-9_]*)\b/,
    });

    ghidraGrammarPatched = true;
    return window.Prism.languages.c;
}

