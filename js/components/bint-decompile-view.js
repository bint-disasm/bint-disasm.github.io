class DecompileView extends HTMLElement {
    constructor() {
        super();
        this.attachShadow({ mode: 'open' }).appendChild(template.content.cloneNode(true));

        this._code = this.shadowRoot.getElementById('code');

        this._client = null;      // DecompilerClient (lazy)
        this._session = null;     // current DecompilerSession
        this._sessionKey = null;  // string key we used to build the session; if the
                                  // binary changes we invalidate and rebuild.
        this._sessionRegions = null; // [[start, end), ...] of session's loaded segments
        this._currentSeek = 0n;
        this._api = null;

        // Visibility state: the parent panel calls setActive(bool) when
        // the decompile tab is shown/hidden. We only kick off decompiles
        // while visible, so flipping to the hex tab doesn't cost anything.
        this._active = false;
        this._debounceTimer = null;
        // Monotonic counter so stale in-flight decompiles can be dropped.
        this._requestId = 0;
        // Address of the last successful decompile — if the new seek lands
        // inside the same region we could skip work, but function
        // boundaries aren't known to this view yet. For now we re-run
        // any time the seek changes to a different address.
        this._lastAddr = null;
        // While locked, SEEK_CHANGED stops moving _currentSeek so
        // the displayed function stays put even as the rest of the
        // UI navigates. Toggled from the parent via setLocked().
        this._locked = false;
    }

    /** Pin / unpin the panel to its current seek. While locked we
     *  ignore SEEK_CHANGED so the user can keep one function on
     *  screen while clicking around in disassembly or names. On
     *  unlock we re-sync to the live seek and re-run the
     *  decompiler. */
    setLocked(locked) {
        const next = !!locked;
        if (next === this._locked) return;
        this._locked = next;
        if (!this._locked) {
            this._syncSeekFromApi().then(() => {
                if (this._active) this._scheduleDecompile();
            });
        }
    }
    isLocked() { return this._locked; }

    connectedCallback() {
        this._api = getWasmAPI();

        events.on(Events.BINARY_LOADED, () => this._onBinaryChanged());
        events.on(Events.BINARY_UNLOADED, () => this._onBinaryChanged());
        events.on(Events.SEEK_CHANGED, (addr) => this._onSeek(addr));
        events.on(Events.SEEK, (addr) => this._onSeek(addr));
    }

    /**
     * Pull the current seek from the wasm session. load_binary sets
     * seek to the entry point but doesn't emit a SEEK event, so without
     * this we'd sit on seek=0 until the user clicked something.
     */
    async _syncSeekFromApi() {
        if (!this._api) return;
        try {
            const addr = await this._api.getSeek();
            const parsed = parseAddress(addr);
            if (parsed !== null) this._currentSeek = parsed;
        } catch (e) {
            // best-effort; fall back to whatever _currentSeek was.
        }
    }

    /**
     * Called by the parent tab strip when this view becomes visible
     * (true) or hidden (false). We defer all actual decompile work
     * until we're active, so sitting on the hex tab is free.
     */
    setActive(active) {
        const wasActive = this._active;
        this._active = !!active;
        if (this._active && !wasActive) {
            // Catch up to whatever seek bint is currently at — we may
            // have missed SEEK events fired while the tab was hidden.
            this._syncSeekFromApi().then(() => this._scheduleDecompile());
        }
    }

    _onSeek(addr) {
        if (this._locked) return;
        // SEEK_CHANGED payloads come as hex strings like "0x4005c5" from
        // api.setSeek(), but also sometimes as raw numbers/bigints from
        // other code paths. Normalize to BigInt — addresses can be
        // 64-bit (e.g. Mach-O AArch64 starts at 0x100000000).
        const parsed = parseAddress(addr);
        if (parsed !== null) this._currentSeek = parsed;
        if (this._active) this._scheduleDecompile();
    }

    _onBinaryChanged() {
        if (this._session) {
            this._session.close().catch(() => {});
            this._session = null;
            this._sessionKey = null;
            this._sessionRegions = null;
        }
        this._lastAddr = null;
        this._code.innerHTML = '<div class="hint">Binary changed.</div>';
        // load_binary set seek to the entry point on the Rust side but
        // doesn't emit a SEEK event; pull it so we show the right address.
        this._syncSeekFromApi().then(() => {
            if (this._active) this._scheduleDecompile();
        });
    }

    _scheduleDecompile() {
        if (this._debounceTimer) clearTimeout(this._debounceTimer);
        this._debounceTimer = setTimeout(() => {
            this._debounceTimer = null;
            this._decompile();
        }, SEEK_DEBOUNCE_MS);
    }

    async _ensureClient(arch) {
        if (!this._client) {
            this._client = new DecompilerClient('/decompiler/worker.js');
            this._loadedArchs = new Set();
        }
        if (!this._loadedArchs.has(arch)) {
            await this._client.init({
                specBaseUrl: new URL('/decompiler/spec/', location.href).toString(),
                manifestUrl: '/decompiler/spec-manifest.json',
                arch,
            });
            this._loadedArchs.add(arch);
        }
        return this._client;
    }

    async _ensureSession() {
        const api = this._api;
        const meta = api.getCurrentMetadata();
        if (!meta) throw new Error('no binary loaded');

        const baseSleigh = api.session?.sleigh_language_id();
        if (!baseSleigh) throw new Error(`unsupported architecture: ${meta.architecture}`);
        const languageId = `${baseSleigh}:${compilerFor(meta.format)}`;
        // The first segment of a SLEIGH id ("x86:LE:64:default") is the
        // top-level spec directory in the manifest. Used to mount only
        // this binary's processor instead of all five.
        const archDir = baseSleigh.split(':')[0];

        // Pull per-segment bytes. The decompiler's load image needs each
        // loadable segment at its true virtual address — a flat
        // file-offset mapping (the previous approach) silently aliased
        // the wrong bytes whenever the entry page didn't equal the first
        // PT_LOAD's page (common on i386 ELF) or when segments were
        // non-contiguous in the file (Mach-O).
        const regions = await this._collectRegions();
        if (regions.length === 0) throw new Error('no loadable segments');

        const entry = parseAddress(meta.entry_point) ?? 0n;
        const totalBytes = regions.reduce((n, r) => n + r.bytes.length, 0);
        const key = `${meta.format}|${meta.architecture}|${totalBytes}|${entry}`;
        if (this._session && this._sessionKey === key) return this._session;

        if (this._session) {
            await this._session.close().catch(() => {});
        }

        // Symbols: pull everything bint knows. Function kinds are what
        // the decompiler can bind prototypes against.
        const symbols = await this._collectSymbols();
        // Readonly regions let the decompiler render pointer derefs into
        // rodata as string literals. Pull them from `info sections /j`.
        const readonly = await this._collectReadonly();
        // Detected printable strings → typed `char[len]` symbols. Hint
        // lets the decompiler render constant references as string
        // literals (e.g. `"abc"` instead of `0x400914`) without
        // requiring user retyping.
        const strings = await this._collectStrings();

        const client = await this._ensureClient(archDir);
        this._session = await client.open({
            languageId,
            regions,
            symbols,
            readonly,
            strings,
        });
        // Cache region extents for the in-image bounds check in
        // _decompile. Stored as [start, end) BigInt pairs so the
        // hot-path comparison stays cheap.
        this._sessionRegions = regions.map((r) => [r.vaddr, r.vaddr + BigInt(r.bytes.length)]);
        this._sessionKey = key;
        return this._session;
    }

    /**
     * Build the list of memory regions to populate the decompiler with.
     * One entry per readable section, with bytes pulled from the wasm
     * session (which knows the segment→file_offset mapping). Skips
     * write-only and zero-size sections.
     */
    _readableSections() {
        // info_sections returns SectionsOutput { sections } with each
        // entry typed as { name, virtual_address: BigInt, virtual_size:
        // BigInt, file_offset, file_size, permissions }. Filters keep
        // empty/zero-VA entries out — those are non-loadable file
        // metadata sections like .symtab.
        try {
            const output = this._api.session.info_sections();
            const sections = output?.sections || [];
            return sections.filter((s) => {
                if (!s.permissions || !s.permissions.includes('r')) return false;
                if (s.virtual_address === 0n || s.virtual_size === 0n) return false;
                return true;
            });
        } catch (e) {
            console.warn('info_sections failed', e);
            return [];
        }
    }

    async _collectRegions() {
        const regions = [];
        for (const s of this._readableSections()) {
            const sizeNum = Number(s.virtual_size);
            if (!Number.isFinite(sizeNum) || sizeNum <= 0) continue;
            const bytes = this._api.readAt(s.virtual_address, sizeNum);
            if (!bytes || bytes.length === 0) continue;
            regions.push({ vaddr: s.virtual_address, bytes });
        }
        return regions;
    }

    async _collectReadonly() {
        // Readonly = readable AND not writable. Strings in .rodata /
        // __const get picked up; .bss / .data don't.
        return this._readableSections()
            .filter((s) => !s.permissions.includes('w'))
            .map((s) => [s.virtual_address, s.virtual_size]);
    }

    /**
     * Detected printable strings, keyed for the decompiler's typed
     * `char[len]` symbols. Min length matches the native side (4)
     * so single-char readable bytes don't pollute the symbol table.
     */
    async _collectStrings() {
        try {
            const out = this._api.session.info_strings(4);
            const list = out?.strings || [];
            return list
                .filter((s) => (s.length ?? 0) > 0)
                .map((s) => [s.address, BigInt(s.length)]);
        } catch (e) {
            console.warn('info_strings failed', e);
            return [];
        }
    }

    async _collectSymbols() {
        // bint's `import_symbols` categorises resolved imports (puts,
        // etc.) as NameKind::Import — distinct from Function — so we
        // pull both via the typed name_list and merge. First-wins on
        // duplicate addresses.
        const out = new Map(); // BigInt address → name
        for (const kind of ['func', 'import']) {
            try {
                const output = this._api.session.name_list(kind);
                for (const e of (output?.entries || [])) {
                    const name = e.name || '';
                    if (e.address === 0n || !name) continue;
                    if (!out.has(e.address)) out.set(e.address, name);
                }
            } catch (e) {
                console.warn(`name_list(${kind}) failed`, e);
            }
        }
        return Array.from(out.entries());
    }

    /**
     * Render decompiler output into the code pane. Uses Prism for C
     * syntax highlighting if it's available; otherwise falls back to
     * plain text so a missing Prism doesn't silently break the panel.
     */
    _renderCode(code) {
        const grammar = ensureGhidraGrammar();
        if (grammar && window.Prism) {
            this._code.innerHTML = window.Prism.highlight(code, grammar, 'c');
        } else {
            this._code.textContent = code;
        }
    }

    async _decompile() {
        const meta = this._api.getCurrentMetadata();
        if (!meta) return;

        // Snap the seek to the containing function's entry so the
        // decompiler gets the start of a real function, not a random
        // byte in the middle. Falls back to the raw seek if the
        // resolver can't identify a function (no names, no matching
        // prologue pattern).
        let addr;
        try {
            addr = parseAddress(this._api.resolveFunctionEntry(this._currentSeek)) ?? this._currentSeek;
        } catch (e) {
            addr = this._currentSeek;
        }

        // Bounds-check against the loaded segments. Out-of-image seeks
        // (e.g. seek=0 on first open) get a friendly hint instead of a
        // worker-side decompile error. If we haven't built a session
        // yet, the regions list is empty — skip the check and let
        // _ensureSession handle the no-segments case.
        const inImage = !this._sessionRegions || this._sessionRegions.length === 0
            || this._sessionRegions.some(([base, end]) => addr >= base && addr < end);
        if (!inImage) {
            this._code.innerHTML =
                '<div class="hint">seek to a function (current address is outside the mapped image).</div>';
            return;
        }

        const myId = ++this._requestId;
        if (this._lastAddr !== addr) {
            this._code.innerHTML = '<div class="hint">decompiling…</div>';
        }
        try {
            const session = await this._ensureSession();
            if (myId !== this._requestId) return; // superseded
            const code = await session.decompile(addr, '');
            if (myId !== this._requestId) return; // superseded
            this._renderCode(code);
            this._lastAddr = addr;
        } catch (e) {
            if (myId !== this._requestId) return;
            this._code.innerHTML = `<div class="hint" style="color: var(--color-error);">error: ${escapeHtml(String(e))}</div>`;
        }
    }
}

customElements.define('bint-decompile-view', DecompileView);
